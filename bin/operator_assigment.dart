// Digunakan untuk memasukkan nilai ke dalam variabel. Selain = standar, ada jalan pintas yang keren di Dart:
void main() {
  int angka = 5;

  angka += 3; // Sama dengan: angka = angka + 3
  print(angka); // Output: 8

  angka *= 2; // Sama dengan: angka = angka * 2
  print(angka); // Output: 16
}
